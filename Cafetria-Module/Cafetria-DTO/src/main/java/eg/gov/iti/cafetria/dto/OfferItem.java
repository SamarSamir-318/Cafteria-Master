package eg.gov.iti.cafetria.dto;

import java.io.Serializable;

/**
 *
 * @author Administrator
 */
public class OfferItem implements Serializable{
    private String name;
    private int price;

    public OfferItem() {
    }

    public OfferItem(String name, int price) {
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
}
